    private void incluirDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_incluirDadosActionPerformed
        
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        var data = LocalDate.parse(this.dataDadosTempo.getText(), formatter);
        var temperatura = Double.parseDouble(this.temperaturaDadosTempo.getText());
        var umidade = Double.parseDouble(this.umidadeDadosTempo.getText());
        var pressao = Double.parseDouble(this.pressaoDadosTempo.getText());
        
        this.tempoPresenter.incluirDados(data, temperatura, umidade, pressao);
        
        this.ultimaData.setText(this.tempoPresenter.getUltimoRegistro().getData().format(formatter));
        this.ultimaPressao.setText(this.tempoPresenter.getUltimoRegistro().getPressao().toString());
        this.ultimaTemperatura.setText(this.tempoPresenter.getUltimoRegistro().getTemperatura().toString());
        this.ultimaUmidade.setText(this.tempoPresenter.getUltimoRegistro().getUmidade().toString());
        
        Vector dadosTabela = new Vector();
        dadosTabela.add(this.tempoPresenter.getUltimoRegistro().getData().format(formatter));
        dadosTabela.add(this.tempoPresenter.getUltimoRegistro().getTemperatura().toString());
        dadosTabela.add(this.tempoPresenter.getUltimoRegistro().getUmidade().toString());
        dadosTabela.add(this.tempoPresenter.getUltimoRegistro().getPressao().toString());
        DefaultTableModel model = (DefaultTableModel) registroTabela.getModel();
        model.addRow(dadosTabela);
        
        dataDadosTempo.setText("");
        umidadeDadosTempo.setText("");
        pressaoDadosTempo.setText("");
        temperaturaDadosTempo.setText("");
        
 
        atualizaTotalRegistros();
        atualizaMedia();
    }//GEN-LAST:event_incluirDadosActionPerformed

    private void atualizaMedia(){
        var opcao = this.selecionadorPeriodo.getSelectedIndex();
        this.tempoPresenter.calculaMedia(opcao);
        
        if (opcao == 0){
            this.temperaturaMedia.setText(this.tempoPresenter.getMediaDiaria().getMediaTemperatura().toString());
            this.pressaoMedia.setText(this.tempoPresenter.getMediaDiaria().getMediaPressao().toString());
            this.umidadeMedia.setText(this.tempoPresenter.getMediaDiaria().getMediaUmidade().toString());
            this.numeroDeRegistrosPeriodo.setText(String.valueOf(this.tempoPresenter.getCountRegistros()));
        }
        
        if (opcao == 1){
            this.temperaturaMedia.setText(this.tempoPresenter.getMediaSemanal().getMediaTemperatura().toString());
            this.pressaoMedia.setText(this.tempoPresenter.getMediaSemanal().getMediaPressao().toString());
            this.umidadeMedia.setText(this.tempoPresenter.getMediaSemanal().getMediaUmidade().toString());
            this.numeroDeRegistrosPeriodo.setText(String.valueOf(this.tempoPresenter.getCountRegistros()));
        }
        
        if (opcao == 2){
            this.temperaturaMedia.setText(this.tempoPresenter.getMediaMensal().getMediaTemperatura().toString());
            this.pressaoMedia.setText(this.tempoPresenter.getMediaMensal().getMediaPressao().toString());
            this.umidadeMedia.setText(this.tempoPresenter.getMediaMensal().getMediaUmidade().toString());
            this.numeroDeRegistrosPeriodo.setText(String.valueOf(this.tempoPresenter.getCountRegistros()));
        }
    }
